package jk.fhws_rooms.data;

public class Module {

    private String label;
    private String url;

    public Module() {
    }

    public String getLabel() {
        return label;
    }

    public String getUrl() {
        return url;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
